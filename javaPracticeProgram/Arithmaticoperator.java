package javaPracticeProgram;

public class Arithmaticoperator 
{
	public static void main(String[] args) 
	{
	int number1=30, number2=15;
	int res=number1+number2;
	
	//addition operation
	int number3=12;
	int number4=3;
	res=number3+number4;
	System.out.println("Addition of two number: "+res);
	
	//Substraction operation
	res = number1-number2;
	System.out.println("Substraction of two number: "+res);
	
	//multiplication operation
	res=number1*number2;
	System.out.println("Multiplication of two number: "+res);
	
	//division of two number
	res=number1/number2;
	System.out.println("Division of two number: "+res);
	//Modulus of two number
	res=number1%number2;
	System.out.println("Modulus of two number: "+res);

	}

}
